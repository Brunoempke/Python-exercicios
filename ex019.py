import math
angulo=float(input('digite um angulo'))
se=math.sin (math.radians(angulo))
print('a angulo de {} tem o seno de {:.2f}'.format(angulo,se))
co=math.cos (math.radians(angulo))
print('o angulo de {} tem o cos de {:.2f}'.format(angulo,co))
tan=math.tan (math.radians(angulo))
print('o angulo de{} tem o tan de {:.2f}'.format(angulo,tan))
